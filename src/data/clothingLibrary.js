// Helper function to get all items in a category
export function getCategoryItems(category) {
  return CLOTHING_LIBRARY[category] || {}
}

// Helper function to search across all categories
export function searchClothingLibrary(searchTerm) {
  const results = []
  const lowerSearch = searchTerm.toLowerCase()

  Object.entries(CLOTHING_LIBRARY).forEach(([category, items]) => {
    Object.entries(items).forEach(([name, image]) => {
      if (name.toLowerCase().includes(lowerSearch)) {
        results.push({ name, image, category })
      }
    })
  })

  return results
}

// Get all items flattened
export function getAllClothingItems() {
  const allItems = []

  Object.entries(CLOTHING_LIBRARY).forEach(([category, items]) => {
    Object.entries(items).forEach(([name, image]) => {
      allItems.push({ name, image, category })
    })
  })

  return allItems
}

